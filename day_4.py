# Data Type:
# it tells which type of data can store in a variable

# primitive datatype:used to store a single value to a single variable 
# immutable

# int:whole numbers (postive/neagtive and zero)
student_count=60
print(type(student_count))

# float:decimal point (any number with a dot)
price=99.50
print(type(price))

# string(str):used to store text. (Any text inside 'single' or "double")

city="Warangal"
print(type(city))

# boolean:boolean represent logic.(only two:True or False)

is_passed="True"

# non-primitive datatype:used to store multiple values into a single variable

# list:mutable (we can modified)
# store multiple values in a single variable using square brackets [] seprated by commas

my_list=["Apple",10,5.5,True]
print(type(my_list))

# tuple:immutable(can't modified)
# store multiple values in a single variable using parenthises () seprated by commas
 
coordinates=(10.5,20.3)

print(type(coordinates))

# dictionary: key-value pairs

laptop={"brand":"hp","ram":"8gb"}
print(type(laptop))


